#include <unistd.h>

int main(int argc, char **argv)
{
    if (argc > 1)
    {
        int len;
        
        len = 0;
        while (argv[1][len])
            len++;
        write(1, argv[1], len);
    }
	write(1, "\n", 1);
} 
